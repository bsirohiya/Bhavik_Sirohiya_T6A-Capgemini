# 3 modes for file handling
    # 1. write(w)
    # 2. read(r)
    # 3. append(a)

# WRITE MODE
    # 1. Only write (w)
    # 2. write + read (w+)
    # 3. write binary (wb)
    # 4. write & read binary (wb+)

# READ MODE
    # 1. Only read (r)
    # 2. read + write (r+)
    # 3. read binary (rb)
    # 4. read & write binary (rb+)

# APPEND MODE 
# 1. Only append (a)
# 2. append + read (a+)
# 3. append binary (ab)
# 4. append & read binary (ab+)